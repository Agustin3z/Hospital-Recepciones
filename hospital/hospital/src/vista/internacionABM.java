    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Internacion i = new Internacion();
        

        Date now = new Date(System.currentTimeMillis());
        SimpleDateFormat fecha = new SimpleDateFormat("yyyy-MM-dd");
        jftffecha.setText(fecha.format(now));
        i.setPaciente(jtfpaciente.getText());
        i.setFecha(Date.valueOf(jftffecha.getText()));
        i.setDiagnostico(jtfdiagnostico.getText());
        i.setId(Integer.parseInt(jtfid.getText()));
        i.setCama((Cama) jcbcama.getSelectedItem());

        try {
            c.agregar_internacion(i, tabla);
            actualizar();
        } catch (SQLException ex) {
            Logger.getLogger(internacionABM.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(internacionABM.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_jButton1ActionPerformed

