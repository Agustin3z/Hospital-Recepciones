    private void tablaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaMouseClicked
        DefaultTableModel tm = (DefaultTableModel) tabla.getModel();
        String paciente = String.valueOf(tm.getValueAt(tabla.getSelectedRow(), 0));
        String fecha = String.valueOf(tm.getValueAt(tabla.getSelectedRow(), 1));
        String diagnostico = String.valueOf(tm.getValueAt(tabla.getSelectedRow(), 2));
        String cama = String.valueOf(tm.getValueAt(tabla.getSelectedRow(), 3));
        String id = String.valueOf(tm.getValueAt(tabla.getSelectedRow(), 4));

        jtfpaciente.setText(paciente);
        jftffecha.setText(fecha);
        jtfdiagnostico.setText(diagnostico);
        jcbcama.setSelectedItem(cama);
        jtfid.setText(id);
        jlbcama.setText(cama); 
        
        
        
        
    }//GEN-LAST:event_tablaMouseClicked

